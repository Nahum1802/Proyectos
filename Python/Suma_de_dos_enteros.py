entero1 = int(input("Introduzca el primer numero entero:\n")) #Aqui se asigna el valor de "entero1" y se le da la instruccion
entero2 = int(input("Introduzca el segundo numero entero:\n")) #Aqui se asigna el valor de "entero2" y se le da la instruccion

resultado = entero1 + entero2 #Se hace la suma para que quede guardado en "resultado"

print(f"La suma de {entero1} mas {entero2} es igual a: {resultado}") #Es el texto de salida, la "f" es para que pueda tomar los valores introducidos por el usuario 